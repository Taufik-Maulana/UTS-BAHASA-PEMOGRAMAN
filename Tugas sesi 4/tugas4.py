#ctype_data_int
pertama = 10
print(pertama)
print(type(pertama))

#type_data_float
kedua = 2.7
print(kedua)
print(type(kedua))

#type_data_tuple
ketiga = 8, 4
print(ketiga)
print(type(ketiga))

#type_data_String
keempat = "ayo pergi"
print(keempat)
print(type(keempat))

#type_data_boolean
angka1 = 30
angka2 = 12
penjumlahan = angka1 + angka2
pengurangan  = angka1 - angka2
print(penjumlahan < pengurangan)
print(type(penjumlahan < pengurangan))

#type_data_complex
B2 = complex(3, 5)
print(B2)
print(type(B2))

#type_data_list
Al = ([3,0,1,2])
print(Al)
print(type(Al))

#type_data_dictionary
Ping = {"nama":"Taufik", 'kelas': 12}
print(Ping)
print(type(Ping))